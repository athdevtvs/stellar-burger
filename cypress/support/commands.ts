Cypress.Commands.add('isInViewport', (item) => {
  cy.get(item).should((item) => {
    const bottom = Cypress.$(cy.state('window')).height() as number;
    const rect = item[0].getBoundingClientRect();

    expect(rect.top).not.to.be.greaterThan(bottom);
    expect(rect.bottom).not.to.be.greaterThan(bottom);
  });
});

Cypress.Commands.add('closeModalByButton', { prevSubject: true }, () => {
  cy.get('[data-cy=close-button]').click();
});

Cypress.Commands.add('closeModalByOverlay', { prevSubject: true }, () => {
  cy.get('body').click(0, 0);
});
